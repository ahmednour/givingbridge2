      locale: localeName,
    );
    final String countString = countNumberFormat.format(count);

    String _temp0 = intl.Intl.pluralLogic(
      count,
      locale: localeName,
      other: '$countString عمليات معلقة',
      one: 'عملية واحدة معلقة',
      zero: 'لا توجد عمليات معلقة',
    );
    return '$_temp0';
  }

